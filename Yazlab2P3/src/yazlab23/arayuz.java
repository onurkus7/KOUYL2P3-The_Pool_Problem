    private void GoruntuleButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GoruntuleButonActionPerformed
        // TODO add your handling code here:

        DefaultTableModel tablo = (DefaultTableModel) hatTablosu.getModel();
        String[][] dugumlerbilgi = new String[tablo.getRowCount()][3];

        for (int i = 0; i < dugumlerbilgi.length; i++) {
            for (int j = 0; j < 3; j++) {
                dugumlerbilgi[i][j] = tablo.getValueAt(i, j).toString();

            }
        }

        for (int i = 0; i < dugumlerbilgi.length; i++) {
            for (int j = 0; j < 3; j++) {
                System.out.print(dugumlerbilgi[i][j] + " ");
            }
            System.out.println(" ");
        }
        System.out.println("=========");
        int kenarsayisi = tablo.getRowCount();

        this.setVisible(false);
        new arayuzGraph(dugumlerbilgi, baslangicNode, bitisNode, dugumsayisi, kenarsayisi).setVisible(true);

    }//GEN-LAST:event_GoruntuleButonActionPerformed

