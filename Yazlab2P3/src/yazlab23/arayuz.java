    private void DuzenleButonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DuzenleButonActionPerformed
        // TODO add your handling code here:
        tabloMesaji.setText("");
        tabloMesaji2.setText("");
        DefaultTableModel tablo = (DefaultTableModel) hatTablosu.getModel();
        int secilen_satir = hatTablosu.getSelectedRow();

        if (secilen_satir == -1) {

            if (hatTablosu.getRowCount() == 0) {
                tabloMesaji2.setText("UYARI:");
                tabloMesaji.setText("Boru Hattı Bigileri Tablosu Boş");
            } else {
                tabloMesaji2.setText("UYARI:");
                tabloMesaji.setText("Düzenlenecek Boru Hattını Seçilmedi");
            }

        } else {

            tablo.setValueAt(HatBasiText.getText().toUpperCase(), secilen_satir, 0);
            tablo.setValueAt(HatSonuText.getText().toUpperCase(), secilen_satir, 1);
            tablo.setValueAt(HatKapasiteText.getText(), secilen_satir, 2);

            tabloMesaji.setText("Boru Hattı Düzenlendi");

        }
    }//GEN-LAST:event_DuzenleButonActionPerformed

